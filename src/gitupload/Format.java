package gitupload;

public class Format {

	public static void main(String[] args) {
		// 서식문자
		System.out.printf("%d",1); // d% 
		System.out.printf("%o",10); // %o
		System.out.printf("%x",14); // %x
		System.out.printf("%f",2.4); // %f
		System.out.printf("%c",'d'); // %c
		System.out.printf("%s","koko"); // %s

	}

}
