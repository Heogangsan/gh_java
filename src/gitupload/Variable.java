package gitupload;

public class Variable {

	public static void main(String[] args) {
		// variable data type
		//1. 정수형
		int intx = 0;
		byte bytez = 0;
		short shortz = 0;
		long longz = 0;
		
		//2. 실수형
		float floatz = 0.0f;
		double doublez = 0.0;
		
		//3. 문자형
		char charz = 'a';
		
		//4 문자열
		String stringz = null;
		
		//5. 논리형
		boolean booleanz = false;
		
		// 예시
		
		int ver = 1;
		double sor = 3.42125;
		char sang = 'A';
		String sns = "kakao";
		boolean check = true;
		
		System.out.println(ver);
		System.out.println(sor);
		System.out.println(sang);
		System.out.println(sns);
		System.out.println(check);
		
//		초기값 : 어떤 값을 넣을 지 아직 모를 때
//		정수:0
//		실수:0.0
//		문자:' '
//		문자열: null 또는 " "
	}

}
